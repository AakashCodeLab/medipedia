<a name="ab"></a> <font face="walter turncoat"  color="black"style ="text-shadow: 4px 4px 4px #aaa;"><h2> About zaleplon</h2></font>
<p>
 Zaleplon is a sedative, also called a hypnotic. It affects chemicals in your brain that may become unbalanced and cause 
 sleep problems (insomnia).

Zaleplon is used to treat insomnia. This medicine causes relaxation to help you fall asleep and stay asleep.

Zaleplon may also be used for purposes not listed in this medication guide.
</p>
<h2>Do NOT use zaleplon if:</h2>
<ul><li>
you are allergic to any ingredient in zaleplon</li>
<li>you are taking sodium oxybate (GHB)</li></ul>
<p>Contact your doctor or health care provider right away if any of these apply to you.</p>
<a name="be"></a>
<font face="walter turncoat"  color="black"style ="text-shadow: 4px 4px 4px #aaa;"><h2> Before taking zaleplon</h2></font>
<p>Some medical conditions may interact with zaleplon.
 Tell your doctor or pharmacist if you have any medical conditions, especially if any of the following apply to you:</p>
<ul>
<li>if you are pregnant, planning to become pregnant, or are breast-feeding</li>
<li>if you are taking any prescription or nonprescription medicine, herbal preparation, or dietary supplement</li>
<li>if you have allergies to medicines, foods, or other substances, especially tartrazine dye or aspirin</li>
<li>if you have severe kidney or liver problems or a breathing disorder</li>
<p>Some MEDICINES MAY INTERACT with zaleplon. Tell your health care provider if you are taking any other medicines,
 especially any of the following:</p>

<li>Cimetidine or sodium oxybate (GHB) because an increase in sleep duration may occur</li>
<li>Carbamazepine, phenobarbital, phenytoin, or rifampin because they may decrease zaleplon's effectiveness</li></ul>
<p>This may not be a complete list of all interactions that may occur. 
Ask your health care provider if zaleplon may interact with other medicines that you take.
 </p>
<a name="how"></a><font face="walter turncoat"  color="black"style ="text-shadow: 4px 4px 4px #aaa;"><h2>  How to take zaleplon</h2></font>

<p>Use zaleplon as directed by your doctor. Check the label on the medicine for exact dosing instructions.</p>
<ul type="I"><li>Zaleplon comes with an extra patient information sheet called a Medication Guide. Read it carefully.
 Read it again each time you get zaleplon refilled.</li>
<li>Take zaleplon by mouth with or without food.</li>
<li>Do not take zaleplon with or right after a high-fat or heavy meal. Doing so may delay when the medicine will begin to work.</li>
<li>Zaleplon works very quickly. It should be taken immediately before going to bed or after you have gone to bed
 and are having trouble falling asleep.</li>
<li>Do not take zaleplon unless you will be able to get 4 or more hours of sleep before you must be active again.</li>
<li>If you miss a dose of zaleplon, take it when you remember if you are still able to get at least 4 hours of sleep.
 If you are no longer able to get at least 4 hours of sleep when you remember, skip the missed dose and go back 
 to your regular dosing schedule. Do not take 2 doses at once.</li></ul>
<p>Ask your health care provider any questions you may have about how to use zaleplon.</p>
 

<a name="can"></a><font face="walter turncoat"  color="black"style ="text-shadow: 4px 4px 4px #aaa;"><h2>Can zaleplon cause problems?</h2></font>				 
<p>All medicines may cause side effects, but many people have no, or minor, side effects.
 Check with your doctor if any of these most COMMON side effects persist or become bothersome:</p>
<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Abnormal skin sensations; decreased sensitivity to touch; dizziness; drowsiness; incoordination;
 lightheadedness; muscle pain; short-term memory loss.</p>

<p>Seek medical attention right away if any of these SEVERE side effects occur:</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Severe allergic reactions (rash; hives; itching; difficulty breathing; tightness in the chest;
 swelling of the mouth, face, lips, or tongue); abnormal thinking; agitation; bizarre behavior; 
 changes in personality, &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;mood, or behavior; daytime drowsiness; hallucinations; memory loss;
 shortness of breath; suicidal thoughts; worsening of depression.</p>

<p>This is not a complete list of all side effects that may occur. If you have questions about side effects, contact your health care provider.
 Call your doctor for medical advice about side effects.</p>
