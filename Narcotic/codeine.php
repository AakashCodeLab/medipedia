<p>Codeine is an <font color="red" style="text-shadow:4px 4px 4px #aaa;">opioid medicine (sometimes called an opiate)</font>.
 It is used to treat mild-to-moderate types of pain. It can be particularly useful when painkillers such as paracetamol and ibuprofen have not been effective. It works by binding to certain tiny areas, called opioid receptors, in your central nervous system (brain and spinal cord).
 This leads to a decrease in the way you feel pain and your reaction to pain. Codeine is available on a prescription from a doctor.

Codeine is also contained in a number of combination medicines, some of which can be bought over-the-counter, 
without a prescription, in pharmacies.
In particular, it is available in combination with the painkiller paracetamol in a medicine called co-codamol. 
There is a separate leaflet called Co-codamol for pain relief which explains about this medicine</p>
