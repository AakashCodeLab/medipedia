<a name="how"></a><font face="walter turncoat"  color="black"style ="text-shadow: 4px 4px 4px #aaa;"><h2>  How to take codeine</h2></font>
<ul type="I"><li>Before you start the treatment, read the manufacturer's printed information leaflet from inside the pack.
 It will give you more information about codeine and
 will provide you with a full list of the side-effects which you may experience 
 from taking it.</li>
<li>Take codeine exactly as your doctor tells you to. The usual dose for pain relief is 30-60 mg every four hours if needed. 
The dose should not be repeated more frequently than every four hours, and it is important that you do not
take more than a maximum daily amount of 240 mg. As there are several different strengths of tablets available,
your doctor or pharmacist will tell you how many tablets to take for each dose.This information will also be printed
on the label of the pack to remind you.</li>
<li>Swallow the tablets with a drink of water. You can take codeine either before or after food, but taking the tablets 
after food can help prevent feelings of sickness which can occur with the first few doses.</li>
<li>If you forget to take a dose, take it as soon as you remember and then continue taking your doses every four hours if needed, 
as before. Do not take two doses together to make up for a forgotten dose.</li></ul>
 

